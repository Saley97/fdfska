#include "libft.h"

void	ft_strsdel(char ***as)
{
	int	i;

	i = -1;
	if (!as || !*as || !**as)
		return ;
	while (as[0][++i])
		free(as[0][i]);
	free(as[0]);
	as[0] = NULL;
}
